    private void drawData()
    {
        List<HashMap<String, String>> HashArray = Collections.list(Collections.enumeration(log.values()));
        ArrayList<LogItem> logArray = new ArrayList<LogItem>();
        keys = new ArrayList<String>(log.keySet());
        for (HashMap<String, String> entry : HashArray)
        {
            LogItem temp = new LogItem();
            temp.restaurant = entry.get("restaurant");
            temp.amount = entry.get("amount");
            temp.date = entry.get("date");

            DateFormat df = new SimpleDateFormat("dd/mm/yyyy");
            try {
                temp.dateObject = df.parse(temp.date);

            } catch (ParseException e) {
                e.printStackTrace();
            }

            temp.whoPaid = entry.get("whoPaid");
            logArray.add(temp);
        }

        Comparator comparator = Collections.reverseOrder();
        Collections.sort(logArray, comparator);

        LogItemAdapter adapter = new LogItemAdapter(this, android.R.layout.simple_list_item_1, logArray);
        ListView listView = (ListView) findViewById(R.id.listview);
        listView.setAdapter(adapter);
        listView.setOnItemLongClickListener(new AdapterView.OnItemLongClickListener() {
            @Override
            public boolean onItemLongClick(AdapterView<?> arg0, View arg1,
                                           int pos, long id) {
                // TODO Auto-generated method stub

                //Log.v("long clicked","pos: " + pos);
                mDatabase.child("Logs").child(keys.get(pos)).removeValue();
                //onStart();
                return true;
            }
        });
    }

